export type Locale = "fr" | "en" | "es";

const LOCALE_KEY = "elevio-locale";
const SUPPORTED: Locale[] = ["fr", "en", "es"];

export function isValidLocale(v: string): v is Locale {
  return SUPPORTED.includes(v as Locale);
}

/** Resolve locale on the server from cookies (set by the client LanguageProvider). */
export function localeFromCookies(cookieStr: string | null): Locale {
  if (!cookieStr) return "fr";
  const match = cookieStr.match(new RegExp(`${LOCALE_KEY}=([a-z]{2})`));
  if (match && isValidLocale(match[1])) return match[1];
  return "fr";
}

export function localeFromNavigatorTag(tag: string): Locale {
  const normalized = tag.trim().toLowerCase();

  if (normalized.startsWith("es")) {
    return "es";
  }

  if (normalized.startsWith("en")) {
    return "en";
  }

  return "fr";
}

